from pydantic import BaseSettings


class Settings(BaseSettings):
    database_hostname: str
    database_port: str
    database_password: str
    database_name: str
    database_username: str
    secret_key: str
    algorithm: str
    access_token_expire_minutes: int
    valid_email_allowed: str
    authority_level: str

    class Config:
        env_file = ".env"


setting = Settings()


class Firebase(BaseSettings):
    type: str
    project_id: str
    private_key_id: str
    private_key: str
    client_email: str
    client_id: str
    auth_uri: str
    token_uri: str
    auth_provider_x509_cert_url: str
    client_x509_cert_url: str
    storage_bucket: str

    class Config:
        env_file = ".env"


firebase_cred = Firebase()
